/** Default template when nothing matches. */
const DEFAULT_TEMPLATE_ID = 'library';

/**
 * Register the `sanix init` command.
 *
 * @param program     - The Commander root program.
 * @param ctxProvider - Lazy context provider (called on first action).
 */
export function registerInitCommand(
  program: Command,
  ctxProvider: () => Promise<SanixContext>,
): void {
  program
    .command('init <description>')
    .description('Scaffold a new project from a natural-language description.')
    .option('--name <name>', 'Project name (default: auto from description)')
    .option('--template <template>', 'Use a specific built-in template')
    .option('--dir <path>', 'Output directory (default: ./<name>)')
    .option('--no-tests', 'Skip test setup')
    .option('--no-docker', 'Skip Dockerfile')
    .option('--no-ci', 'Skip CI/CD setup')
    .option('--git', 'Initialize a git repo after scaffolding')
    .option('--json', 'Output JSON of created files')
    .action(async (description: string, opts: InitCommandOptions) => {
      try {
        const ctx = await ctxProvider();
        await initCommand(ctx, description, opts);
      } catch (err) {
        const msg = err instanceof Error ? err.message : String(err);
        // eslint-disable-next-line no-console
        console.error(chalk.red(`\n✗ sanix init failed: ${msg}\n`));
        process.exitCode = 1;
      }
    });
}

/**
 * Run the `sanix init` command. Exposed for programmatic use.
 *
 * @param ctx         - The wired SANIX context.
 * @param description - The natural-language project description.
 * @param opts        - Parsed CLI options.
 */
export async function initCommand(
  ctx: SanixContext,
  description: string,
  opts: InitCommandOptions,
): Promise<void> {
  // 1. Detect the template (explicit > auto-detect > default).
  const template = pickTemplate(description, opts.template);
  // 2. Derive the project name.
  const name = opts.name ?? deriveName(description, template.id);
  const outDir = resolve(opts.dir ?? `./${name}`);
  // 3. Build the files.
  const buildCtx: TemplateBuildCtx = {
    name,
    description,
    tests: opts.tests !== false,
    docker: opts.docker !== false,
    ci: opts.ci !== false,
  };
  let files: ScaffoldFile[];
  let llmUsed = false;
  try {
    const llmFiles = await tryLlmScaffold(ctx, description, template, buildCtx);
    if (llmFiles) {
      files = llmFiles;
      llmUsed = true;
    } else {
      files = template.build(buildCtx);
    }
  } catch {
    files = template.build(buildCtx);
  }

  // 4. Write the files.
  if (existsSync(outDir) && existsSync(join(outDir, 'package.json')) && template.id !== 'library') {
    // For app templates, refuse to overwrite an existing project.
    throw new Error(`Output directory ${outDir} already contains a package.json. Aborting.`);
  }
  mkdirSync(outDir, { recursive: true });
  const written: Array<{ path: string; bytes: number }> = [];
  for (const f of files) {
    const fp = join(outDir, f.path);
    const dir = join(fp, '..');
    mkdirSync(dir, { recursive: true });
    if (existsSync(fp) && !f.overwrite) {
      // Skip existing files unless overwrite is set.
      continue;
    }
    writeFileSync(fp, f.content, 'utf-8');
    written.push({ path: f.path, bytes: Buffer.byteLength(f.content, 'utf-8') });
  }

  // 5. Optionally init git.
  if (opts.git) {
    const r = spawnSync('git', ['init'], { cwd: outDir, stdio: 'ignore' });
    if (r.status === 0) {
      // Initial commit.
      spawnSync('git', ['add', '-A'], { cwd: outDir, stdio: 'ignore' });
      spawnSync('git', ['commit', '-m', `chore: initial commit (scaffolded by sanix init)`], {
        cwd: outDir,
        stdio: 'ignore',
      });
    }
  }

  // 6. Report.
  if (opts.json) {
    // eslint-disable-next-line no-console
    console.log(JSON.stringify({
      template: template.id,
      name,
      dir: outDir,
      llmUsed,
      files: written,
    }, null, 2));
    return;
  }
  // eslint-disable-next-line no-console
  console.log(chalk.hex('#00D4FF')(`\n✓ Scaffolded ${chalk.cyan(name)} (${template.id}) → ${chalk.dim(outDir)}`));
  // eslint-disable-next-line no-console
  console.log(chalk.dim(`  ${llmUsed ? 'LLM-generated' : 'template-based'} • ${written.length} files\n`));
  for (const w of written) {
    // eslint-disable-next-line no-console
    console.log(`  ${chalk.green('create')} ${w.path} ${chalk.dim(`(${w.bytes} B`)}`);
  }
  // eslint-disable-next-line no-console
  console.log(chalk.hex('#FFB347')('\nNext steps:'));
  // eslint-disable-next-line no-console
  console.log(chalk.dim(`  cd ${basename(outDir)}`));
  if (template.id === 'python-api') {
    // eslint-disable-next-line no-console
    console.log(chalk.dim('  python -m venv .venv && source .venv/bin/activate'));
    // eslint-disable-next-line no-console
    console.log(chalk.dim('  pip install -r requirements.txt'));
  } else {
    // eslint-disable-next-line no-console
    console.log(chalk.dim('  npm install   # or: bun install / pnpm install'));
  }
  // eslint-disable-next-line no-console
  console.log('');
}

/** Pick a template by id, or auto-detect from the description. */
function pickTemplate(description: string, explicit?: string): TemplateDef {
  if (explicit) {
    const t = TEMPLATES.find((x) => x.id === explicit);
    if (!t) {
      throw new Error(
        `Unknown template "${explicit}". Available: ${TEMPLATES.map((x) => x.id).join(', ')}`,
      );
    }
    return t;
  }
  const lower = description.toLowerCase();
  for (const t of TEMPLATES) {
    if (t.matches(lower)) return t;
  }
  const def = TEMPLATES.find((x) => x.id === DEFAULT_TEMPLATE_ID)!;
  return def;
}

/** Derive a project name from the description or template. */
function deriveName(description: string, templateId: string): string {
  // Try to find a "called X" or "named X" in the description.
  const m = description.match(/(?:called|named)\s+["']?([a-zA-Z0-9_-]+)/i);
  if (m && m[1]) return m[1].toLowerCase();
  // Fallback: template-based default.
  const defaults: Record<string, string> = {
    'express-api': 'my-api',
    'next-app': 'my-app',
    'react-app': 'my-react-app',
    'python-api': 'my-api',
    'cli-tool': 'my-cli',
    library: 'my-lib',
  };
  return defaults[templateId] ?? 'my-project';
}

/**
 * Try to use the LLM to generate a richer scaffold. Returns `null` if no
 * LLM is available or the call fails. We give the LLM the description +
 * template id + project name, and ask it to return a JSON object mapping
 * file paths to contents. If the LLM fails or returns invalid JSON, we
 * fall back to the built-in template.
 */
async function tryLlmScaffold(
  ctx: SanixContext,
  description: string,
  template: TemplateDef,
  buildCtx: TemplateBuildCtx,
): Promise<ScaffoldFile[] | null> {
  // First, build the template files so we always have a baseline.
  const baseline = template.build(buildCtx);
  // Only attempt LLM enhancement when the router has at least one provider.
  let providers: { id: string }[] = [];
  try {
    providers = ctx.router.list().map((p) => ({ id: p.id }));
  } catch {
    providers = [];
  }
  if (providers.length === 0) return null;

  const systemPrompt = `You are a senior software architect. Generate project scaffold files based on the user's description. Return ONLY a JSON object mapping file paths to file contents. No prose, no markdown fences. Keys are relative paths (POSIX), values are the full file contents as strings. Keep each file focused and minimal but realistic.`;
  const userPrompt = `Project name: ${buildCtx.name}
Description: ${description}
Base template: ${template.id}
Include tests: ${buildCtx.tests}
Include Docker: ${buildCtx.docker}
Include CI: ${buildCtx.ci}

Generate a complete project. Return a JSON object: { "path/to/file.ext": "file content" }.`;

  try {
    const res = await ctx.router.route({
      messages: [
        { role: 'system', content: systemPrompt },
        { role: 'user', content: userPrompt },
      ],
      taskType: 'code',
      maxTokens: 8000,
    });
    const text = res.content?.trim() ?? '';
    if (!text) return null;
    // Try to parse the JSON. The model may have wrapped it in fences despite
    // the instruction — strip them defensively.
    const cleaned = text
      .replace(/^```(?:json)?\s*/i, '')
      .replace(/\s*```$/i, '')
      .trim();
    const parsed = JSON.parse(cleaned) as unknown;
    if (typeof parsed !== 'object' || parsed === null) return null;
    const files: ScaffoldFile[] = [];
    for (const [path, content] of Object.entries(parsed as Record<string, unknown>)) {
      if (typeof content !== 'string') continue;
      files.push({ path, content });
    }
    if (files.length === 0) return null;
    // Merge: prefer LLM-generated paths, but always include a README if missing.
    const have = new Set(files.map((f) => f.path));
    for (const b of baseline) {
      if (!have.has(b.path)) files.push(b);
    }
    return files;
  } catch {
    return null;
  }
}

