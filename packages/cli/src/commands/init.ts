/**
 * @file commands/init.ts
 * @description `sanix init "<description>"` — natural-language project scaffolder.
 *
 *   sanix init "REST API with Express, PostgreSQL, JWT auth"
 *     --name <project-name>      Project name (default: auto from description)
 *     --template <template>      Use a specific built-in template
 *     --dir <path>               Output directory (default: ./<name>)
 *     --no-tests                 Skip test setup
 *     --no-docker                Skip Dockerfile
 *     --no-ci                    Skip CI/CD setup
 *     --git                      Initialize a git repo
 *     --json                     JSON output of created files
 *
 * The command detects the user's intent from the description (language,
 * framework, database, auth, features), then generates a complete
 * project skeleton. If an LLM provider is configured + reachable, the
 * file contents are generated by the LLM; otherwise the built-in
 * templates are used.
 *
 * Built-in templates:
 *   - `express-api`  — Express + TypeScript + PostgreSQL + JWT + tests
 *   - `next-app`     — Next.js + Tailwind + Prisma + Auth.js
 *   - `react-app`    — Vite + React + TypeScript + Tailwind
 *   - `python-api`   — FastAPI + SQLAlchemy + JWT + pytest
 *   - `cli-tool`     — Commander.js + TypeScript + tests
 *   - `library`      — TypeScript library with tsup + vitest
 *
 * @packageDocumentation
 */

import type { Command } from 'commander';
import chalk from 'chalk';
import { join, resolve, basename } from 'node:path';
import { existsSync, mkdirSync, writeFileSync } from 'node:fs';
import { spawnSync } from 'node:child_process';
import type { SanixContext } from '../bootstrap.js';

/** Parsed options for `sanix init`. */
export interface InitCommandOptions {
  name?: string;
  template?: string;
  dir?: string;
  tests?: boolean;
  docker?: boolean;
  ci?: boolean;
  git?: boolean;
  json?: boolean;
}

/** A single file in a scaffolded project. */
export interface ScaffoldFile {
  /** Relative path from the project root (e.g. `src/index.ts`). */
  path: string;
  /** File content. */
  content: string;
  /** Whether to overwrite an existing file (default: false). */
  overwrite?: boolean;
}

/** Description of a built-in template. */
export interface TemplateDef {
  /** Template id (matches `--template`). */
  id: string;
  /** Human-readable name. */
  name: string;
  /** One-line description. */
  description: string;
  /** Returns true if the description matches this template. */
  matches: (desc: string) => boolean;
  /** Build the files for the template. */
  build: (ctx: TemplateBuildCtx) => ScaffoldFile[];
}

/** Context passed to a template's `build` function. */
export interface TemplateBuildCtx {
  /** Project name. */
  name: string;
  /** Original description. */
  description: string;
  /** Whether tests are included. */
  tests: boolean;
  /** Whether Docker is included. */
  docker: boolean;
  /** Whether CI is included. */
  ci: boolean;
}

