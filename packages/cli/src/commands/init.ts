/** Build the `express-api` template. */
function buildExpressApi(c: TemplateBuildCtx): ScaffoldFile[] {
  const files: ScaffoldFile[] = [];
  files.push({
    path: 'package.json',
    content: JSON.stringify({
      name: c.name,
      version: '0.1.0',
      type: 'module',
      scripts: {
        dev: 'tsx watch src/index.ts',
        build: 'tsc',
        start: 'node dist/index.js',
        test: c.tests ? 'vitest run' : undefined,
        lint: 'eslint src/',
      },
      dependencies: {
        express: '^4.19.0',
        pg: '^8.12.0',
        jsonwebtoken: '^9.0.0',
        bcryptjs: '^2.4.0',
        zod: '^3.24.0',
        cors: '^2.8.5',
        dotenv: '^16.4.0',
      },
      devDependencies: {
        typescript: '^5.6.0',
        '@types/express': '^4.17.0',
        '@types/node': '^22.0.0',
        '@types/pg': '^8.11.0',
        '@types/jsonwebtoken': '^9.0.0',
        '@types/bcryptjs': '^2.4.0',
        '@types/cors': '^2.8.0',
        tsx: '^4.19.0',
        ...(c.tests ? { vitest: '^2.1.0', supertest: '^7.0.0', '@types/supertest': '^6.0.0' } : {}),
      },
    }, null, 2),
  });
  files.push({
    path: 'tsconfig.json',
    content: JSON.stringify({
      compilerOptions: {
        target: 'ES2022',
        module: 'ESNext',
        moduleResolution: 'Bundler',
        outDir: './dist',
        rootDir: './src',
        strict: true,
        esModuleInterop: true,
        skipLibCheck: true,
        resolveJsonModule: true,
      },
      include: ['src/**/*'],
      exclude: ['node_modules', 'dist', 'test'],
    }, null, 2),
  });
  files.push({
    path: '.env.example',
    content: [
      '# Database',
      'DATABASE_URL=postgres://user:pass@localhost:5432/mydb',
      '',
      '# Auth',
      'JWT_SECRET=change-me-in-production',
      'JWT_EXPIRES_IN=24h',
      '',
      '# Server',
      'PORT=3000',
      'NODE_ENV=development',
      '',
    ].join('\n'),
  });
  files.push({
    path: '.gitignore',
    content: ['node_modules/', 'dist/', '.env', '*.log', '.DS_Store', 'coverage/'].join('\n') + '\n',
  });
  files.push({
    path: 'src/index.ts',
    content: [
      "import express from 'express';",
      "import cors from 'cors';",
      "import 'dotenv/config';",
      "import { userRoutes } from './routes/users.js';",
      "import { authRoutes } from './routes/auth.js';",
      '',
      'const app = express();',
      'const PORT = process.env.PORT ?? 3000;',
      '',
      'app.use(cors());',
      'app.use(express.json());',
      '',
      "app.get('/health', (_req, res) => res.json({ status: 'ok' }));",
      "app.use('/auth', authRoutes);",
      "app.use('/users', userRoutes);",
      '',
      'app.listen(PORT, () => {',
      '  console.log(`Server listening on http://localhost:${PORT}`);',
      '});',
      '',
    ].join('\n'),
  });
  files.push({
    path: 'src/db.ts',
    content: [
      "import { Pool } from 'pg';",
      '',
      'export const pool = new Pool({',
      '  connectionString: process.env.DATABASE_URL,',
      '});',
      '',
    ].join('\n'),
  });
  files.push({
    path: 'src/routes/auth.ts',
    content: [
      "import { Router } from 'express';",
      "import jwt from 'jsonwebtoken';",
      "import bcrypt from 'bcryptjs';",
      "import { pool } from '../db.js';",
      '',
      'export const authRoutes = Router();',
      '',
      'authRoutes.post("/register", async (req, res) => {',
      '  const { email, password } = req.body as { email: string; password: string };',
      '  if (!email || !password) return res.status(400).json({ error: "email + password required" });',
      '  const hash = await bcrypt.hash(password, 10);',
      '  const result = await pool.query(',
      '    "INSERT INTO users (email, password_hash) VALUES ($1, $2) RETURNING id, email",',
      '    [email, hash],',
      '  );',
      '  res.status(201).json(result.rows[0]);',
      '});',
      '',
      'authRoutes.post("/login", async (req, res) => {',
      '  const { email, password } = req.body as { email: string; password: string };',
      '  const result = await pool.query("SELECT * FROM users WHERE email = $1", [email]);',
      '  const user = result.rows[0];',
      '  if (!user) return res.status(401).json({ error: "invalid credentials" });',
      '  const ok = await bcrypt.compare(password, user.password_hash);',
      '  if (!ok) return res.status(401).json({ error: "invalid credentials" });',
      '  const token = jwt.sign({ sub: user.id }, process.env.JWT_SECRET!, {',
      '    expiresIn: process.env.JWT_EXPIRES_IN ?? "24h",',
      '  });',
      '  res.json({ token });',
      '});',
      '',
    ].join('\n'),
  });
  files.push({
    path: 'src/routes/users.ts',
    content: [
      "import { Router } from 'express';",
      "import jwt from 'jsonwebtoken';",
      "import { pool } from '../db.js';",
      '',
      'export const userRoutes = Router();',
      '',
      'userRoutes.use((req, res, next) => {',
      '  const header = req.headers.authorization;',
      '  if (!header?.startsWith("Bearer ")) return res.status(401).json({ error: "no token" });',
      '  try {',
      '    const payload = jwt.verify(header.slice(7), process.env.JWT_SECRET!);',
      '    (req as unknown as { user: unknown }).user = payload;',
      '    next();',
      '  } catch {',
      '    res.status(401).json({ error: "invalid token" });',
      '  }',
      '});',
      '',
      'userRoutes.get("/me", async (req, res) => {',
      '  const payload = (req as unknown as { user: { sub: string } }).user;',
      '  const result = await pool.query("SELECT id, email FROM users WHERE id = $1", [payload.sub]);',
      '  res.json(result.rows[0]);',
      '});',
      '',
    ].join('\n'),
  });
  if (c.tests) {
    files.push({
      path: 'test/health.test.ts',
      content: [
        "import { describe, it, expect } from 'vitest';",
        '',
        'describe("placeholder", () => {',
        '  it("passes", () => {',
        '    expect(1 + 1).toBe(2);',
        '  });',
        '});',
        '',
      ].join('\n'),
    });
  }
  if (c.docker) {
    files.push({
      path: 'Dockerfile',
      content: [
        'FROM node:20-alpine AS builder',
        'WORKDIR /app',
        'COPY package*.json ./',
        'RUN npm ci',
        'COPY . .',
        'RUN npm run build',
        '',
        'FROM node:20-alpine',
        'WORKDIR /app',
        "ENV NODE_ENV=production",
        'COPY --from=builder /app/dist ./dist',
        'COPY --from=builder /app/node_modules ./node_modules',
        'COPY package*.json ./',
        'EXPOSE 3000',
        'CMD ["node", "dist/index.js"]',
        '',
      ].join('\n'),
    });
    files.push({
      path: 'docker-compose.yml',
      content: [
        'services:',
        '  api:',
        '    build: .',
        '    ports: ["3000:3000"]',
        '    env_file: .env',
        '    depends_on: [db]',
        '  db:',
        '    image: postgres:16-alpine',
        '    environment:',
        '      POSTGRES_USER: user',
        '      POSTGRES_PASSWORD: pass',
        '      POSTGRES_DB: mydb',
        '    ports: ["5432:5432"]',
        '    volumes: ["pgdata:/var/lib/postgresql/data"]',
        'volumes:',
        '  pgdata:',
        '',
      ].join('\n'),
    });
  }
  if (c.ci) {
    files.push({
      path: '.github/workflows/ci.yml',
      content: [
        'name: CI',
        'on: [push, pull_request]',
        'jobs:',
        '  test:',
        '    runs-on: ubuntu-latest',
        '    services:',
        '      postgres:',
        '        image: postgres:16',
        '        env: { POSTGRES_PASSWORD: pass, POSTGRES_DB: mydb }',
        '        ports: ["5432:5432"]',
        '    steps:',
        '      - uses: actions/checkout@v4',
        '      - uses: actions/setup-node@v4',
        '        with: { node-version: "20" }',
        '      - run: npm ci',
        '      - run: npm run build',
        '      - run: npm test',
        '',
      ].join('\n'),
    });
  }
  files.push({
    path: 'README.md',
    content: [
      `# ${c.name}`,
      '',
      c.description,
      '',
      '## Getting started',
      '',
      '```bash',
      'npm install',
      'cp .env.example .env  # then edit values',
      'npm run dev',
      '```',
      '',
      '## Endpoints',
      '',
      '- `GET /health` — health check',
      '- `POST /auth/register` — register a new user',
      '- `POST /auth/login` — login, returns a JWT',
      '- `GET /users/me` — current user (requires `Authorization: Bearer <token>`)',
      '',
    ].join('\n'),
  });
  return files;
}

